CREATE TABLE users (
    user_id integer PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY ( INCREMENT 1 START 1 MINVALUE 1 MAXVALUE 2147483647 CACHE 1 ),
    user_name VARCHAR(255) UNIQUE NOT NULL,
    user_email VARCHAR(255) UNIQUE NOT NULL,
    user_password_hash VARCHAR(255) NOT NULL,
    active BOOLEAN NOT NULL DEFAULT false,
    role VARCHAR(50) NOT NULL CHECK (role IN ('normal', 'admin'))
);

CREATE TABLE posts(
    post_id integer PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY ( INCREMENT 1 START 1 MINVALUE 1 MAXVALUE 2147483647 CACHE 1 ),
    user_id INTEGER NOT NULL,
    post_title VARCHAR(255) NOT NULL,
    post_content VARCHAR(255) NOT NULL, 
    post_likes NUMERIC(6)
);

CREATE TABLE comments(
    post_id INTEGER NOT NULL,
    user_id INTEGER NOT NULL,
    comment_content VARCHAR(255) NOT NULL
);